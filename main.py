def decode_absolute_leet(text):
    leet_dict = {
        '/-\\': ['A'], '/\\': ['A'], '4': ['A', 'H'], '@': ['A'], '^': ['A'], 'λ': ['A'], 'α': ['A'], 'д': ['A'],
        '|3': ['B'], '13': ['B'], 'I3': ['B'], '8': ['B', 'X', 'R'], '6': ['B', 'G'], 'ß': ['B'], '[3': ['B'], '33': ['B'],
        '(': ['C'], '<': ['C'], '[': ['C'], '¢': ['C'], '©': ['C'], 'sea': ['C'],
        '|)': ['D'], '|>': ['D'], '[)': ['D'], 'I>': ['D'], '0': ['O', 'D'], '〕': ['D'], 'cl': ['D'],
        '3': ['E', 'B'], '&': ['E', 'G'], '£': ['E', 'L'], '€': ['E'], 'ε': ['E'], '[-': ['E'], 'I=': ['E'],
        '|=': ['F'], 'ƒ': ['F'], '|#': ['F'], 'ph': ['F'], '/=': ['F'],
        '(_+': ['G'], '6': ['G', 'B'], '9': ['G', 'P', 'Q'], '&': ['G', 'E'], 'C-': ['G'], 'gee': ['G'],
        '|-|': ['H'], ']-[': ['H'], '}-{': ['H'], '(-)': ['H'], ')-(': ['H'], '#': ['H'], '[-]': ['H'], '!-!': ['H'], '1-1': ['H'],
        '1': ['I', 'L', 'T', 'J'], '!': ['I', 'L'], '|': ['I', 'L'], '][': ['I'], '¡': ['I'], 'eye': ['I'], '3y3': ['I'],
        ',_|': ['J'], '_|': ['J'], '._]': ['J'], ';': ['J'],
        '|<': ['K'], '/<': ['K'], '\\<': ['K'], '|{': ['K'], '1<': ['K'],
        '|_': ['L'], '7': ['T', 'L', 'Z'], '£': ['L', 'E'],
        '|\\/|': ['M'], '/\\/\\': ['M'], '|\'|\'|': ['M'], '(\\/)': ['M'], '/\\\\': ['M'], '/|\\': ['M'], '/v\\': ['M'], '[V]': ['M'], '^^': ['M'],
        '|\\|': ['N'], '/\\/': ['N'], '|\\\\|': ['N'], '/|/': ['N'], '/v': ['N'], '[\\ ]': ['N'], '^/': ['N'], 'и': ['N'],
        '()': ['O'], '[]': ['O'], '{}': ['O'], 'oh': ['O'], '<>': ['O'], 'Ø': ['O'], 'ø': ['O'],
        '|2': ['P', 'R'], '|D': ['P'], '|*': ['P'], '|o': ['P'], '|º': ['P'], '7\'': ['P'],
        '(,)': ['Q'], '(_,)': ['Q'], '9\'': ['Q'], '¶': ['Q'], 'kw': ['Q'],
        '|Z': ['R'], '|?': ['R'], '12': ['R'], 'i2': ['R'], '®': ['R'], 'я': ['R'], 'lz': ['R'],
        '5': ['S', 'Z'], '$': ['S'], '§': ['S'], 'ehs': ['S'], 'z': ['S'],
        '-|-': ['T'], '\'][\'': ['T'], '†': ['T'], '«|»': ['T'], '+': ['T'],
        '|_|': ['U'], '[_]': ['U'], 'L|': ['U'], 'v': ['U', 'V'], 'μ': ['U'],
        '\\/': ['V'], '|/': ['V'], '\\|': ['V'],
        '\\/\\/': ['W'], 'vv': ['W'], '\\N': ['W'], '\'//': ['W'], 'พ': ['W'], '₩': ['W'],
        '><': ['X'], '}{': ['X'], '×': ['X'], ')(': ['X'], 'ecks': ['X'],
        '`/': ['Y'], '`|': ['Y'], '¥': ['Y'], '\\//': ['Y'], 'γ': ['Y'],
        '2': ['Z', 'R'], '7_': ['Z'], '≥': ['Z']
    }
    
    sorted_keys = sorted(leet_dict.keys(), key=len, reverse=True)
    decoded_chars = []
    i = 0
    
    while i < len(text):
        matched = False
        
        for key in sorted_keys:
            length = len(key)
            if text[i:i+length].lower() == key.lower():
                options = leet_dict[key]
                if len(options) == 1:
                    decoded_chars.append(options[0])
                else:
                    decoded_chars.append(f"({'/'.join(options)})")
                i += length
                matched = True
                break
                
        if not matched:
            decoded_chars.append(text[i].upper())
            i += 1
            
    return "".join(decoded_chars)

texto_codificado = "/-\\ |3 ( |) 3 |= (_+ |-| 1 _| |< |_ |\\/| |\\| 0 |2 (,) |2 5 7 |_| \\/ \\/\\/ >< `/ 2"

print(decode_absolute_leet(texto_codificado))
